package com.kata.Kata;

public class TemperatureRange {
    private int min;
    private int max;

    public TemperatureRange(int min, int max) {
        this.min = min;
        this.max = max;
    }


    public int getMin() {
        return min;
    }

    public int getMax() {
        return max;
    }
}
